/* Mix array & Array of Array */
let arr = ["xyz", 10, ["raj", "dev"], [1,2,3,4,5], "simran"];
console.log(arr[1]);        // 10
console.log(arr[2][1]);     // dev
console.log(arr[3][3]);     // 4
console.log(arr[3][2]);     // 3
console.log(arr[4]);        // simran